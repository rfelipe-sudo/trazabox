-- ── PASO 6: Habilitar RLS (Row Level Security) ───────────────
ALTER TABLE supervisores_traza ENABLE ROW LEVEL SECURITY;
ALTER TABLE supervisor_tecnicos_traza ENABLE ROW LEVEL SECURITY;

-- Política: lectura pública (la app usa anon key)
CREATE POLICY "Lectura pública supervisores"
  ON supervisores_traza FOR SELECT USING (true);

CREATE POLICY "Lectura pública supervisor_tecnicos"
  ON supervisor_tecnicos_traza FOR SELECT USING (true);

-- ── VERIFICACIÓN ─────────────────────────────────────────────
SELECT cargo, COUNT(*) as total FROM supervisores_traza GROUP BY cargo;
SELECT rut_supervisor, COUNT(*) as tecnicos
  FROM supervisor_tecnicos_traza
  GROUP BY rut_supervisor ORDER BY rut_supervisor;
