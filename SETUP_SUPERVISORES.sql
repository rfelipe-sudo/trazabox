-- ── PASO 1: Agregar columna rol a tecnicos_traza_zc ─────────
ALTER TABLE tecnicos_traza_zc
  ADD COLUMN IF NOT EXISTS rol TEXT DEFAULT 'tecnico';

-- ── PASO 2: Tabla de supervisores ───────────────────────────
CREATE TABLE IF NOT EXISTS supervisores_traza (
  id          BIGSERIAL PRIMARY KEY,
  rut         TEXT UNIQUE NOT NULL,
  nombre      TEXT,
  email       TEXT,
  cargo       TEXT DEFAULT 'supervisor', -- 'supervisor' | 'ito'
  activo      BOOLEAN DEFAULT TRUE,
  created_at  TIMESTAMPTZ DEFAULT NOW(),
  updated_at  TIMESTAMPTZ DEFAULT NOW()
);

-- ── PASO 3: Tabla relación supervisor ↔ técnico ─────────────
-- Usada para asistencia técnica en terreno (futuro)
CREATE TABLE IF NOT EXISTS supervisor_tecnicos_traza (
  id             BIGSERIAL PRIMARY KEY,
  rut_supervisor TEXT NOT NULL,
  rut_tecnico    TEXT NOT NULL,
  created_at     TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (rut_supervisor, rut_tecnico)
);

CREATE INDEX IF NOT EXISTS idx_sup_tec_supervisor
  ON supervisor_tecnicos_traza (rut_supervisor);
CREATE INDEX IF NOT EXISTS idx_sup_tec_tecnico
  ON supervisor_tecnicos_traza (rut_tecnico);

